    private void but_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_LoginActionPerformed
        
        //Get the values of the username and password and send it to the class controller
        String passw = this.jPasswordField1.getText();
        String username = this.tx_username.getText();
        
        boolean isValid;
        Controller contr = new Controller();
        //Creem un objecte controller i li passem al constructor la password i el usuari per a comprovar les credencials. 
        contr.setController(username,passw);
        isValid = contr.isValid();
        
        if (isValid) {
            //Is valid, but is it an admin or is it an user? 
            if (contr.getUser().isAdmin()) {
                AdminFrame af = new AdminFrame();
                af.show();
                af.setConn(contr.getConn());
                af.setController(contr);
                dispose();
            } else {
                UserFrame uf = new UserFrame();
                uf.show();
                uf.setConn(contr.getConn());
                uf.setController(contr);
                dispose();
            }
        } else {
            //Creem el message d'informaci√≥ login error
            JOptionPane.showMessageDialog(null, "INCORRECT USERNAME OR PASSWORD", "INCORRECT VALIDATION", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_but_LoginActionPerformed

